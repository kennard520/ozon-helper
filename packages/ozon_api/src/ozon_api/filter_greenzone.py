SCHOOL = ["школ", "пенал", "тетрад", "канцеляр", "папк", "ручк", "карандаш",
          "дневник", "ученик", "первокласс"]


def _window(q: str) -> str:
    if any(s in q for s in SCHOOL):
        return "学校季"
    if any(s in q for s in SUMMER):
        return "夏季"
    return "四季"


def _pct_rank(values: list[float]) -> list[float]:
    order = sorted(range(len(values)), key=lambda i: values[i])
    n = len(values)
    out = [0.0] * n
    for pos, idx in enumerate(order):
        out[idx] = pos / (n - 1) if n > 1 else 0.0
    return out


def main() -> None:
    rows = [json.loads(l) for l in SRC.open(encoding="utf-8")]
    kept = []
    for r in rows:
        q = (r.get("query") or "").lower()
        if not q:
            continue
        if any(b in q for b in BLOCK):
            continue
        if not any(g in q for g in GREEN):
            continue
        if (r.get("uniqSellers", 0) or 0) <= 0:  # 卖家=0 多为导航/品牌词
            continue
        kept.append(r)
    print(f"[i] {len(rows)} → 绿区白名单准入后 {len(kept)} 行")

    dps = [r.get("count", 0) / max(r.get("uniqSellers", 0) or 1, 1) for r in kept]
    gap = [r.get("zrShare", 0) or 0 for r in kept]
    dem = [r.get("count", 0) or 0 for r in kept]
    r_dps, r_gap, r_dem = _pct_rank(dps), _pct_rank(gap), _pct_rank(dem)

    recs = []
    for i, r in enumerate(kept):
        q = r.get("query", "")
        opp = 100 * (0.4 * r_dps[i] + 0.3 * r_gap[i] + 0.3 * r_dem[i])
        recs.append({
            "机会分": round(opp, 1),
            "窗口": _window(q.lower()),
            "词数": len(q.split()),
            "搜索词": q,
            "搜索量": r.get("count", 0),
            "卖家数": r.get("uniqSellers", 0),
            "需求/卖家比": round(dps[i], 1),
            "缺口率%": round(r.get("zrShare", 0) or 0, 1),
            "加购均价₽": round(r.get("avgCaRub", 0) or 0),
            "年趋势%": round(r.get("trendForYear", 0) or 0, 1),
        })
    recs.sort(key=lambda x: x["机会分"], reverse=True)

    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "绿区候选(按机会分)"
    cols = list(recs[0].keys())
    ws.append(cols)
    for r in recs:
        ws.append([r[c] for c in cols])
    fill = PatternFill("solid", fgColor="1F4E78")
    for c in range(1, len(cols) + 1):
        ws.cell(1, c).font = Font(bold=True, color="FFFFFF")
        ws.cell(1, c).fill = fill
    ws.freeze_panes = "A2"
    for i, w in enumerate([8, 8, 6, 36, 10, 8, 12, 9, 12, 9], 1):
        ws.column_dimensions[get_column_letter(i)].width = w
    wb.save(OUT)
    print(f"[+] 导出 {len(recs)} 行 -> {OUT}\n")

    longtail = [r for r in recs if r["词数"] >= 2]
    print(f"=== 多词长尾 Top 30（绿区初筛后，词数≥2）===")
    print(f"{'机会':>5} {'窗口':<5} {'搜索词':<32} {'量':>6} {'卖家':>4} {'需求/卖家':>8} {'缺口%':>6} {'均价₽':>7}")
    for r in longtail[:30]:
        print(f"{r['机会分']:>5} {r['窗口']:<5} {r['搜索词'][:32]:<32} {r['搜索量']:>6} "
              f"{r['卖家数']:>4} {r['需求/卖家比']:>8} {r['缺口率%']:>6} {r['加购均价₽']:>7}")


if __name__ == "__main__":
    main()
